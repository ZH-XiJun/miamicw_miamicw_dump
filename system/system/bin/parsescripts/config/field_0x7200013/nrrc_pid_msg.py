def Nrps_Get_MsgId_Str( SendPid, RcvPid, MsgId):
    MsgId = 0x0000FFFF & MsgId
    
    if SendPid == "NRRM":
        SendPid="NRRC"

    if RcvPid == "NRRM":
        RcvPid="NRRC"
    
    strMsg_merge = '%s_%s_0x%x'%(SendPid, RcvPid, MsgId)
    strMsg_merge_d = '%s_%s_%d'%(SendPid, RcvPid, MsgId)
    for MsgIdIndex in nrps_all_msg_enum_table.keys():
        if MsgIdIndex.upper() == strMsg_merge.upper() or MsgIdIndex.upper() == strMsg_merge_d.upper():
            return nrps_all_msg_enum_table[MsgIdIndex]

    return "UnknowId"
def Nrps_Get_Pid_Str(pid):

    for pidIndex in nrps_all_pid_enum_table.keys():
        if pidIndex == pid:
            return nrps_all_pid_enum_table[pidIndex]

    return "unknownPid"
